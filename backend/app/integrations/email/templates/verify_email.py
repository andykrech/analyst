"""
Шаблон письма подтверждения регистрации.
"""


def render_verify_email(link: str) -> tuple[str, str]:
    """
    Рендер темы и тела письма подтверждения email.

    Args:
        link: URL для подтверждения (одноразовая ссылка).

    Returns:
        (subject, body) — тема и текст письма.
    """
    subject = "Подтверждение регистрации"
    body = f"""Здравствуйте!

Ваш адрес электронной почты был указан при регистрации в системе "Аналитик". Для подтверждения регистрации перейдите по этой ссылке

{link}
"""
    return subject, body
